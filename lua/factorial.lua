-- Recursive function for getting the factorial of a number n
function recfactorial(n)
    if n >= 1 then 
        return(n * recfactorial(n - 1))
    else
        return 1
    end
end

-- Iterative function for getting the factorial of a number n
function itfactorial(n)
    local x =  1
    if n > 0 then
        for i = n, 2, -1 do        
            x = x * i        
        end
    end
    return x
end